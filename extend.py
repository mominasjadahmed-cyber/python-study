#insert
l1 =[12,43,565,'sun']
# The insert is used to add the number to the list of the first int indicates the position and second is used to the int to add
l1.insert (1,55) 
print(l1)

#append
l1 =[12,43,565,'sun']
# The append is used to add the number to the list of the end
l1.append (49) 
print(l1)


#extend
l1 =[12,43,565,'sun']
# The extend is used to add the multiple number to the list of the end
l1.extend ([50,34,54]) 
print(l1)

#clear

l1=[12,43,565,'sun']
l1.clear()
print(l1)